// Control flow example using switch
let day = "Wednesday"; // Represents the day of the week

switch (day) {
  case "Monday":
    console.log("It's Monday");
    break;
  case "Tuesday":
    console.log("It's Tuesday");
    break;
  case "Wednesday":
    console.log("It's Wednesday");
    break;
  case "Thursday":
    console.log("It's Thursday");
    break;
  case "Friday":
    console.log("It's Friday");
    break;
  case "Saturday":
    console.log("It's Saturday");
    break;
  case "Sunday":
    console.log("It's Sunday");
    break;
  default:
    console.log("Invalid day");
}